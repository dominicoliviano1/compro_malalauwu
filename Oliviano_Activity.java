package oliviano_activity;

public class Oliviano_Activity  {
    public static void main(String [] args) {
    
        int hihi = 90;
        int hehe = 900;
        float total = 8;
        
       
        
        float add = hihi + hehe;
        System.out.println("Addition:       " + add);
        
        float sub = hihi - hehe;
        System.out.println("Subtraction:       " + sub);
        
        float multi = hihi * hehe;
        System.out.println("Multiplication:       " + multi);
        
        float div = hehe / hihi;
        System.out.println("Division:       " + div);
        
        float per = hihi * total;
        System.out.println("Percentage:    " + per + "%");
        
        System.out.println ("Subtraction Increment:    " + --hihi);
        System.out.println ("Addition Increment:    " + ++hehe);
        
    }
    
}
